/**
 * Build-time tracer for the sky-writing greeting.
 *
 * Reads the single-stroke word SVGs, resamples each path into points spaced
 * EVENLY BY TRUE ARC LENGTH, and writes a data file the app imports. Doing the
 * arc-length math here (offline) means the browser never calls getPointAtLength
 * during the animation, so the plane + tracing run at a constant speed with
 * almost no runtime cost.
 *
 * Re-run after changing the SVGs:  npm run trace
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { svgPathProperties } from 'svg-path-properties'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ASSETS = join(__dirname, '..', 'src', 'assets')
const OUT = join(__dirname, '..', 'src', 'data', 'greeting-traced.js')

const FILES = [
  'happy-fathers-day-1.svg',
  'happy-fathers-day-2.svg',
  'happy-fathers-day-3.svg',
]

const STEP = 3 // target spacing between output points, in user units
const MAX_POINTS = 600 // cap per word to keep the data file small
const PAD = 18 // viewBox padding around the ink

const round = (v) => Math.round(v * 10) / 10

function extractD(svg) {
  const m = svg.match(/\sd="([\s\S]*?)"/)
  if (!m) throw new Error('no <path d="..."> found')
  return m[1].replace(/\s+/g, ' ').trim()
}

// Split into subpaths on absolute moveto so pen-lifts become separate strokes.
function splitSubpaths(d) {
  return d
    .split(/(?=M)/)
    .map((s) => s.trim())
    .filter(Boolean)
}

// Dense oversample of a subpath, re-parameterized by true (chord) distance so
// it's uniform regardless of the library's internal parameterization.
function buildFine(sub) {
  const props = new svgPathProperties(sub)
  const total = props.getTotalLength()
  if (!(total > 0)) return null

  const fine = Math.max(200, Math.round(total * 4))
  const X = new Array(fine + 1)
  const Y = new Array(fine + 1)
  const CUM = new Array(fine + 1)
  CUM[0] = 0
  for (let k = 0; k <= fine; k++) {
    const p = props.getPointAtLength((total * k) / fine)
    X[k] = p.x
    Y[k] = p.y
    if (k > 0) CUM[k] = CUM[k - 1] + Math.hypot(X[k] - X[k - 1], Y[k] - Y[k - 1])
  }
  return { X, Y, CUM, fine, trueLen: CUM[fine] }
}

// Resample a fine table into n+1 points spaced EVENLY by true arc length.
function resampleEven(tab, n) {
  const { X, Y, CUM, fine, trueLen } = tab
  const out = []
  let j = 1
  for (let i = 0; i <= n; i++) {
    const target = (trueLen * i) / n
    while (j < fine && CUM[j] < target) j++
    const lo = CUM[j - 1]
    const hi = CUM[j]
    const t = hi > lo ? (target - lo) / (hi - lo) : 0
    out.push({ x: X[j - 1] + (X[j] - X[j - 1]) * t, y: Y[j - 1] + (Y[j] - Y[j - 1]) * t })
  }
  return out
}

function trace(d) {
  const tables = splitSubpaths(d).map(buildFine).filter(Boolean)

  // Pick how many points each subpath gets: ~STEP apart, but if that would
  // exceed MAX_POINTS, allocate the budget proportional to each subpath's
  // length. Both paths keep the spacing UNIFORM (no index-stride downsampling).
  const natural = tables.map((t) => Math.max(2, Math.round(t.trueLen / STEP)))
  let intervals = natural
  if (natural.reduce((a, n) => a + n + 1, 0) > MAX_POINTS) {
    const sumLen = tables.reduce((a, t) => a + t.trueLen, 0)
    const avail = MAX_POINTS - tables.length // reserve 1 point per subpath
    intervals = tables.map((t) => Math.max(2, Math.round((avail * t.trueLen) / sumLen)))
  }

  const points = [] // flat [x0,y0,x1,y1,...]
  const moveSet = new Set() // point indices that begin a new subpath (pen lift)
  tables.forEach((tab, i) => {
    moveSet.add(points.length / 2)
    for (const p of resampleEven(tab, intervals[i])) points.push(round(p.x), round(p.y))
  })

  // bbox + tightened viewBox
  let minX = Infinity
  let minY = Infinity
  let maxX = -Infinity
  let maxY = -Infinity
  for (let i = 0; i < points.length; i += 2) {
    const x = points[i]
    const y = points[i + 1]
    if (x < minX) minX = x
    if (x > maxX) maxX = x
    if (y < minY) minY = y
    if (y > maxY) maxY = y
  }
  const viewBox = `${round(minX - PAD)} ${round(minY - PAD)} ${round(
    maxX - minX + PAD * 2,
  )} ${round(maxY - minY + PAD * 2)}`

  // polyline path: M at each subpath start, L otherwise
  let polyD = ''
  for (let i = 0; i < points.length / 2; i++) {
    const cmd = i === 0 || moveSet.has(i) ? 'M' : 'L'
    polyD += `${cmd}${points[i * 2]} ${points[i * 2 + 1]}`
  }

  return { viewBox, polyD, points }
}

const words = FILES.map((f) => {
  const svg = readFileSync(join(ASSETS, f), 'utf8')
  const w = trace(extractD(svg))
  console.log(`${f}: ${w.points.length / 2} points`)
  return w
})

const header = `// AUTO-GENERATED by scripts/build-greeting.mjs — do not edit by hand.
// Run \`npm run trace\` to regenerate after changing the word SVGs.
`
writeFileSync(OUT, `${header}export const words = ${JSON.stringify(words)}\n\nexport default words\n`)
console.log(`wrote ${OUT}`)
